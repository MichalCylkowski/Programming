public class Dog {

    String breed;

    int age;

    String color;

    public Dog(String breed, int age, String color){
        this.breed = breed;

        this.age = age;

        this.color = color;
    }

    void bark(){
        System.out.println("The dog is barking.");
    }

    void sleep(){
        System.out.println("The dog is sleeping.");
    }




}
